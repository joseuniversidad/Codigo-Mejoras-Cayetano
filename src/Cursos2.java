public class Cursos2 {
    private String nombre;
    private int Idcodigo;

    public Cursos2(String nombre, int Idcodigo) {
        this.nombre = nombre;
        this.Idcodigo = Idcodigo;
    }

    public String getNombre() {
        return nombre;
    }

    public int getIdcodigo() {
        return Idcodigo;
    }
}
